import time
print(time.strftime("%Y"))  #2024
print(time.strftime("%y"))  #24
print(time.strftime("%m"))  #08 for August
print(time.strftime("%B"))  #August
print(time.strftime("%b"))  #Aug
print(time.strftime("%d"))  # 28 day of Aug
print(time.strftime("%A"))  # wednesday
print(time.strftime("%a"))  #wed
print(time.strftime("%d"))  # 28 day of Aug
print(time.strftime("%w")) 
print(time.strftime("%H"))  #24 Hours
print(time.strftime("%I"))  #12 Hours
print(time.strftime("%p"))  #am or pm
print(time.strftime("%S"))  #seconds
print(time.strftime("%X:%p")) #exact local time
print(time.strftime("%x")) #exact date reprsentation


print(time.strftime("%A %B %d , %Y , %I:%m:%S:%p" ))

print(time.strftime("%A, %B %d, %Y %I:%M %p"))
