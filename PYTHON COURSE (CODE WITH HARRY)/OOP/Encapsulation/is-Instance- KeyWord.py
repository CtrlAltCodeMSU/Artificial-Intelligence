# Example 1: Checking if a number is an instance of int
num = 42
print(isinstance(num, int))  # Output: True

# Example 2: Checking if a string is an instance of str
name = 21
print(isinstance(name, str))  # Output: True

# Example 3: Checking if a list is an instance of list
fruits = ["apple", "banana", "cherry"]
print(isinstance(fruits, list))  # Output: True

# Example 4: Using a tuple of types to check multiple types
value = 3.14
print(isinstance(value, (int, float)))  # Output: True (since 3.14 is a float)
